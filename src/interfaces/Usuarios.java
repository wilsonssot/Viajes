    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        String var3 = txtUsuario.getText();
        String var4 = txtContraseña.getText();
        conexionViaje cc = new conexionViaje();
        Connection cn = cc.conectar();
        String sql = "";
        sql = "select * from usuarios";
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                String var1 = rs.getString("USU_CEDULA");
                String var2 = rs.getString("USU_CLAVE");
                String var5 = rs.getString("USU_PERFIL");
                if (var1.equals(var3) && var2.equals(var4)) {
                    if (var5.equals("SECRETARIA")) {
                        this.dispose();
                        Menu m = new Menu();
                        m.jMenu2.setEnabled(false);
                        m.setExtendedState(MAXIMIZED_BOTH);
                        m.setVisible(true);
                    } else if (var5.equals("ADMINISTRADO")) {
                        this.dispose();
                        Menu m = new Menu();
                        m.setExtendedState(MAXIMIZED_BOTH);
                        m.setVisible(true);
                    }

                } else {
                    lblError.setVisible(true);
                    txtContraseña.setText("");
                    txtContraseña.requestFocus();

                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

